# Ensure handover.md is gitignored
GITIGNORE="${REPO_ROOT}/.gitignore"
if [ -f "$GITIGNORE" ]; then
  if ! grep -qxF "handover.md" "$GITIGNORE"; then
    echo "handover.md" >> "$GITIGNORE"
  fi
else
  echo "handover.md" > "$GITIGNORE"
fi

# Set sentinel so Stop hook enforces completion
