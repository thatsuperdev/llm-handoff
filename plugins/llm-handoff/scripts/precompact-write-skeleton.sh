#!/usr/bin/env bash
# Fires before Claude Code compacts the conversation.
# Writes a deterministic skeleton to handover.md and sets a sentinel so the
# Stop hook knows to enforce completion before Claude next stops responding.
set -e

REPO_ROOT=$(git rev-parse --show-toplevel 2>/dev/null || pwd)
HANDOVER_FILE="${REPO_ROOT}/handover.md"
SENTINEL_DIR="${HOME}/.claude/llm-handoff"
SENTINEL="${SENTINEL_DIR}/completion-required"

mkdir -p "$SENTINEL_DIR"

# Collect deterministic git state
BRANCH=$(git branch --show-current 2>/dev/null || echo "unknown")
LAST_COMMIT=$(git log --oneline -1 2>/dev/null || echo "no commits")
GIT_STATUS=$(git status --short 2>/dev/null || echo "unable to get status")
RECENT_COMMITS=$(git log --oneline -5 2>/dev/null || echo "no commits")
WORKTREES=$(git worktree list 2>/dev/null | tail -n +2 | awk '{print $1}' || echo "N/A")
TIMESTAMP=$(date -u +"%Y-%m-%dT%H:%M:%SZ")

# Write skeleton — LLM will complete all <!-- TODO --> sections via Stop hook enforcement
cat > "$HANDOVER_FILE" << SKELETON
---
generated: ${TIMESTAMP}
previous_llm: <!-- TODO: LLM — state your model name and version -->
tool: Claude Code
trigger: auto (PreCompact)
branch: ${BRANCH}
worktree: ${WORKTREES:-N/A}
---

